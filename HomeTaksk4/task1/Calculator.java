package task1;

public class Calculator {

    public int add(int a, int b) {
        return (a + b);
    }

    public double add(double a, double b, double c) {
        return (a + b + c);
    }

    public int subtract(int a, int b) {
        return (a - b);
    }

    public double subtract(double a, double b, double c) {
        return (a - b - c);
    }

}
